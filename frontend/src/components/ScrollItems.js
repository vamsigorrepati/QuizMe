export const Dates = () => {
  const daysOfWeek = ['S', 'M', 'T', 'W', 'T', 'F', 'S'];
  const today = new Date();
  const currentDay = today.getDay();
  const currentWeek = [];

  for (let i = 0; i < 7; i++) {
    const date = today.getDate() + i - currentDay;
    const isCurrent = i === currentDay;

    currentWeek.push({
      day: daysOfWeek[i],
      date: date > 0 ? date : getLastDayOfPreviousMonth(today) + date,
      current: isCurrent,
    });
  }

  return currentWeek;
};

const getLastDayOfPreviousMonth = (date) => {
  const firstDayOfCurrentMonth = new Date(date.getFullYear(), date.getMonth(), 1);
  const lastDayOfPreviousMonth = new Date(firstDayOfCurrentMonth - 1);
  return lastDayOfPreviousMonth.getDate();
};

